	public float NextFloat(){
		place =  (place + alter_value)%values.Length;
		x = (x + 1.0f)%max_x;
		y = (y + 1.0f)%max_y;
		return PerlinNum(max_x, x, y, NextNum(),NextNum(),NextNum(),NextNum());
	}

	private float mix(float a, float b, float weight)
	{
		return  (a * (1.0f - weight))+ (b * weight);
	}

	private float blend(float t)
	{
		float t_3 = t * t * t;
		return  (6 * t * t - 15 * t + 10) * t_3;
	}
		
	private float PerlinNum(float side, float placeX, float placeY, float bottomLeft, float bottomRight, 
		                    float topLeft, float topRight){

		float pi = 3.14159f;
		float thetatl = pi*(topLeft/127.5f);
		float thetatr = pi*(topRight/127.5f);
		float thetabl = pi*(bottomLeft/127.5f);
		float thetabr = pi*(bottomRight/127.5f);
		//Vector2variable.Normalized();
		//float normalize = 1;
		Vector2 gradtl = new Vector2(Mathf.Sin(thetatl), Mathf.Cos(thetatl)).normalized;
		Vector2 gradtr = new Vector2(Mathf.Sin(thetatr), Mathf.Cos(thetatr)).normalized;
		Vector2 gradbl = new Vector2(Mathf.Sin(thetabl), Mathf.Cos(thetabl)).normalized;
		Vector2 gradbr = new Vector2(Mathf.Sin(thetabr), Mathf.Cos(thetabr)).normalized;

		// needs correction
		Vector2 diftl = new Vector2(placeX, placeY-side).normalized;
		Vector2 diftr = new Vector2(placeX-side, placeY-side).normalized;
		Vector2 difbl = new Vector2(placeX, placeY).normalized;
		Vector2 difbr = new Vector2(placeX-side, placeY).normalized;

		float s= gradbl.x*difbl.x + gradbl.y*difbl.y;
		float t= gradbr.x*difbr.x + gradbr.y*difbr.y;
		float u= gradtl.x*diftl.x + gradtl.y*diftl.y;
		float v= gradtr.x*diftr.x + gradtr.y*diftr.y;

		//--- Interpolation parameter t (along x and y)
		float t_x = (placeX) / (side);
		float t_y = (placeY) / (side);
		float st = mix(s, t, blend(t_x));
		float uv = mix(u, v, blend(t_x));;
		// change to ridgid perlin noise
		float noise = Mathf.Abs(mix(st, uv, blend(t_y)));
		if (noise>1.0f){noise = 1.0f;}
		return noise;
	}
